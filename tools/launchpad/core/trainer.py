# Stdout/stderr pumps + parsing
# ---------------------------------------------------------------------------


def _parse_metrics_from_line(line: str, task_id: str) -> tuple[int | None, list[TrainMetric]]:
    """Pull (step, [metrics]) from a single stdout line.

    Returns (step_if_present, list_of_metrics). The caller threads `step` across
    lines so metrics on a subsequent line are tagged with the last-seen step.
    """
    step: int | None = None
    step_match = _STEP_RE.search(line)
    if step_match is not None:
        try:
            step = int(step_match.group("step"))
        except ValueError:
            step = None

    metrics: list[TrainMetric] = []
    t_wall = time.time()
    for m in _METRIC_KV_RE.finditer(line):
        name = m.group("name")
        if name in {"step", "global_step", "global-step", "iter", "iteration"}:
            continue
        if not _is_metric_name(name):
            continue
        try:
            value = float(m.group("value"))
        except ValueError:
            continue
        metrics.append(
            TrainMetric(
                task_id=task_id, step=step if step is not None else 0,
                t_wall=t_wall, name=name, value=value,
            )
        )
    return step, metrics


async def _pump_stream(
    task: _Task, stream: asyncio.StreamReader | None, kind: str
) -> None:
    """Read one stdio stream, fan out to event queues + (stdout only) metric queues."""
    if stream is None:
        return
    try:
        while True:
            try:
                raw = await stream.readline()
            except asyncio.CancelledError:
                raise
            except (ValueError, OSError) as exc:
                # readline may raise on giant lines or closed transport.
                logger.warning("task %s: %s read error: %s", task.task_id, kind, exc)
                break
            if not raw:
                break
            try:
                line = raw.decode("utf-8", errors="replace").rstrip("\n")
            except UnicodeDecodeError:
                line = raw.decode("latin-1", errors="replace").rstrip("\n")

            event = TaskEvent(
                task_id=task.task_id,
                kind="stdout" if kind == "stdout" else "stderr",
                t_wall=time.time(),
                payload={"line": line},
            )
            await _publish_event(task, event)

            if kind == "stdout":
                step, metrics = _parse_metrics_from_line(line, task.task_id)
                if step is not None:
                    task.current_step = step
                # Tag any unstepped metrics with last-known step.
                for met in metrics:
                    if met.step == 0 and task.current_step:
                        met = met.model_copy(update={"step": task.current_step})
                    await _publish_metric(task, met)
    except asyncio.CancelledError:
        logger.debug("task %s: %s pump cancelled", task.task_id, kind)
        raise


async def _pump_finished(task: _Task) -> None:
    """Wait for process exit and broadcast a `finished` event."""
    exit_code: int = -1
    try:
        exit_code = await task.process.wait()
    except asyncio.CancelledError:
        raise
    finally:
        ec = task.process.returncode if task.process.returncode is not None else exit_code
        # Wait for stdout/stderr pumps to drain so consumers see all output
        # before the terminal `finished` event.
        for sub in task.reader_tasks:
            if sub is asyncio.current_task():
                continue
            try:
                await asyncio.wait_for(asyncio.shield(sub), timeout=1.0)
            except (asyncio.TimeoutError, asyncio.CancelledError):
                pass
            except Exception as exc:  # pump-internal failure is non-fatal here
                logger.warning("task %s: pump await error: %s", task.task_id, exc)
        final = TaskEvent(
            task_id=task.task_id,
            kind="finished",
            t_wall=time.time(),
            payload={"exit_code": ec},
        )
        await _publish_event(task, final)
        await _mark_finished(task)

    logger.info("task %s exited with code %d", task.task_id, exit_code)


