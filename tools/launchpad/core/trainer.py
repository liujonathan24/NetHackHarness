"""Spawn `prime rl` / `prime gepa` subprocesses; parse metrics.

`prime rl` is local-only (it's the deprecated alias for `prime train`; takes a
TOML config positional). `prime gepa` is also local-only. Both stream stdout
that we tail for scalar metrics.

Public API (per `tools/launchpad/CONTRACTS.md` section 5):
    materialize_rl_toml(spec, dest) -> Path
    launch_rl(spec, repo_root) -> str
    launch_gepa(spec, repo_root) -> str
    stream_metrics(task_id) -> AsyncIterator[TrainMetric]
    stream_events(task_id) -> AsyncIterator[TaskEvent]
    stop_task(task_id) -> bool
"""

from __future__ import annotations

import asyncio
import json
import logging
import os
import re
import shutil
import signal
import tempfile
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, AsyncIterator

from tools.launchpad.types import TaskEvent, TrainMetric, TrainSpec

logger = logging.getLogger(__name__)

# Grace period before escalating SIGTERM -> SIGKILL.
_KILL_GRACE_S: float = 5.0

# Regexes for parsing scalar metrics from stdout.
# Matches `name=value`, `name: value`, or `name value` segments with floats.
_METRIC_KV_RE = re.compile(
    r"(?P<name>[A-Za-z_][A-Za-z0-9_/\.]*)\s*[:=]\s*(?P<value>-?\d+(?:\.\d+)?(?:[eE][+-]?\d+)?)"
)
_STEP_RE = re.compile(
    r"\b(?:step|global[_-]?step|iter(?:ation)?)\s*[:=]\s*(?P<step>\d+)", re.IGNORECASE
)
# Known scalar metric names we forward; anything matching `eval/...` also passes.
_KNOWN_METRIC_NAMES: frozenset[str] = frozenset(
    {
        "loss",
        "kl",
        "reward",
        "grad_norm",
        "lr",
        "entropy",
        "pg_loss",
        "value_loss",
        "policy_loss",
        "advantage",
    }
)


def _is_metric_name(name: str) -> bool:
    if name in _KNOWN_METRIC_NAMES:
        return True
    if name.startswith("eval/") or name.startswith("eval_"):
        return True
    # GEPA population stats, e.g. `pop/best`, `pop/mean`, `gepa/reward`.
    if name.startswith("pop/") or name.startswith("gepa/"):
        return True
    return False


@dataclass
class _Task:
    """Tracking record for one launched subprocess.

    Subscribers (stream_metrics / stream_events) get a replay of everything
    seen so far plus all future items; this avoids subscribe-race losses if
    the pumps drain stdout before the consumer subscribes.
    """

    task_id: str
    process: asyncio.subprocess.Process
    started_at: float
    current_step: int = 0
    metric_log: list[TrainMetric] = field(default_factory=list)
    event_log: list[TaskEvent] = field(default_factory=list)
    finished: bool = False
    metric_cond: asyncio.Condition | None = None
    event_cond: asyncio.Condition | None = None
    reader_tasks: list[asyncio.Task[None]] = field(default_factory=list)


# Registry. Module-global per CONTRACTS section 8 (registries are the allowed
# exception to the "no global state" rule).
_TASKS: dict[str, _Task] = {}


# ---------------------------------------------------------------------------
# TOML materialization
# ---------------------------------------------------------------------------


def _toml_escape(s: str) -> str:
    """Escape a string for a TOML basic-string literal."""
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")


def _atomic_write_text(dest: Path, text: str) -> None:
    """Write `text` to `dest` atomically (tempfile in same dir + rename)."""
    dest.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_path_str = tempfile.mkstemp(
        prefix=f".{dest.name}.", suffix=".tmp", dir=str(dest.parent)
    )
    tmp_path = Path(tmp_path_str)
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as f:
            f.write(text)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, dest)
    except BaseException:
        try:
            tmp_path.unlink()
        except FileNotFoundError:
            pass
        raise


def materialize_rl_toml(spec: TrainSpec, dest: Path) -> Path:
    """Write a `prime rl`-compatible TOML config from `spec` to `dest`.

    Raises:
        ValueError: if `spec.mode != 'rl'` or required fields are missing.
    """
    if spec.mode != "rl":
        raise ValueError(f"materialize_rl_toml: expected mode='rl', got {spec.mode!r}")
    if not spec.base_model:
        raise ValueError("materialize_rl_toml: spec.base_model is required for mode='rl'")
    if spec.hparams is None:
        raise ValueError("materialize_rl_toml: spec.hparams is required for mode='rl'")

    hp = spec.hparams
    lines: list[str] = []
    lines.append(f'# Auto-generated by tools.launchpad.core.trainer for label={spec.label!r}')
    lines.append("")
    lines.append("[run]")
    lines.append(f'label = "{_toml_escape(spec.label)}"')
    lines.append(f'harness = "{_toml_escape(spec.harness)}"')
    lines.append("")
    lines.append("[model]")
    lines.append(f'base = "{_toml_escape(spec.base_model)}"')
    lines.append("")
    lines.append("[hparams]")
    lines.append(f"lr = {hp.lr!r}")
    lines.append(f"kl_coef = {hp.kl_coef!r}")
    lines.append(f"group_size = {hp.group_size}")
    lines.append(f"rollouts_per_example = {hp.rollouts_per_example}")
    lines.append(f"max_turns = {hp.max_turns}")
    lines.append(f"batch_size = {hp.batch_size}")
    lines.append("")
    lines.append("[env]")
    lines.append("name = \"nethack\"")
    if spec.tiers:
        tiers_arr = ", ".join(f'"{_toml_escape(t)}"' for t in spec.tiers)
        lines.append(f"tiers = [{tiers_arr}]")
    if spec.filtering is not None:
        lines.append("")
        lines.append("[filtering]")
        lines.append(f"min_difficulty = {spec.filtering.min_difficulty!r}")
        lines.append(f"max_difficulty = {spec.filtering.max_difficulty!r}")
        lines.append(f"oversample_hard = {str(spec.filtering.oversample_hard).lower()}")
    if spec.eval is not None:
        lines.append("")
        lines.append("[eval]")
        lines.append(f"every_steps = {spec.eval.every_steps}")
        lines.append(f"n_examples = {spec.eval.n_examples}")
        if spec.eval.tiers:
            tiers_arr = ", ".join(f'"{_toml_escape(t)}"' for t in spec.eval.tiers)
            lines.append(f"tiers = [{tiers_arr}]")

    text = "\n".join(lines) + "\n"
    _atomic_write_text(dest, text)
    logger.info("materialize_rl_toml: wrote %s (%d bytes)", dest, len(text))
    return dest


# ---------------------------------------------------------------------------
# Subprocess launch
# ---------------------------------------------------------------------------


def _ensure_prime_on_path() -> str:
    """Return the absolute path to `prime`, or raise FileNotFoundError."""
    prime = shutil.which("prime")
    if prime is None:
        raise FileNotFoundError("`prime` CLI not found on PATH")
    return prime


def _make_task_id(label: str) -> str:
    return f"{label}_{int(time.time())}"


def _register_task(
    task_id: str, process: asyncio.subprocess.Process, started_at: float
) -> _Task:
    task = _Task(
        task_id=task_id,
        process=process,
        started_at=started_at,
        metric_cond=asyncio.Condition(),
        event_cond=asyncio.Condition(),
    )
    _TASKS[task_id] = task
    # Spawn the reader pumps now (so output flows even with no consumers).
    task.reader_tasks.append(
        asyncio.create_task(_pump_stream(task, process.stdout, "stdout"))
    )
    task.reader_tasks.append(
        asyncio.create_task(_pump_stream(task, process.stderr, "stderr"))
    )
    task.reader_tasks.append(asyncio.create_task(_pump_finished(task)))
    return task


async def _publish_event(task: _Task, event: TaskEvent) -> None:
    assert task.event_cond is not None
    async with task.event_cond:
        task.event_log.append(event)
        task.event_cond.notify_all()


async def _publish_metric(task: _Task, metric: TrainMetric) -> None:
    assert task.metric_cond is not None
    async with task.metric_cond:
        task.metric_log.append(metric)
        task.metric_cond.notify_all()


async def _mark_finished(task: _Task) -> None:
    assert task.event_cond is not None and task.metric_cond is not None
    task.finished = True
    async with task.event_cond:
        task.event_cond.notify_all()
    async with task.metric_cond:
        task.metric_cond.notify_all()


async def launch_rl(spec: TrainSpec, repo_root: Path) -> str:
    """Launch `prime rl <toml>`. Returns task_id."""
    if spec.mode != "rl":
        raise ValueError(f"launch_rl: expected mode='rl', got {spec.mode!r}")
    prime = _ensure_prime_on_path()

    toml_dir = repo_root / "runs" / "rl" / spec.label
    toml_path = toml_dir / "config.toml"
    materialize_rl_toml(spec, toml_path)

    argv = [prime, "rl", str(toml_path)]
    env = os.environ.copy()
    env["NETHACK_HARNESS"] = spec.harness

    logger.info("launch_rl: argv=%s", argv)
    process = await asyncio.create_subprocess_exec(
        *argv,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
        cwd=str(repo_root),
        env=env,
    )
    task_id = _make_task_id(spec.label)
    _register_task(task_id, process, time.time())
    return task_id


async def launch_gepa(spec: TrainSpec, repo_root: Path) -> str:
    """Launch `prime gepa run nethack ...`. Returns task_id."""
    if spec.mode != "gepa":
        raise ValueError(f"launch_gepa: expected mode='gepa', got {spec.mode!r}")
    if not spec.proposer_model:
        raise ValueError("launch_gepa: spec.proposer_model is required for mode='gepa'")
    prime = _ensure_prime_on_path()

    population = spec.population or 4
    generations = spec.generations or 4
    max_calls = population * generations
    # `n_examples` source: prefer eval.n_examples; fall back to 8.
    n_examples = spec.eval.n_examples if spec.eval is not None else 8

    env_args_json = json.dumps({}, separators=(",", ":"))

    run_dir = repo_root / "runs" / "gepa" / spec.label

    argv = [
        prime,
        "gepa",
        "run",
        "nethack",
        "--env-args",
        env_args_json,
        "--model",
        spec.proposer_model,
        "--reflection-model",
        spec.proposer_model,
        "--max-calls",
        str(max_calls),
        "--num-train",
        str(n_examples),
        "--num-val",
        str(n_examples),
        "--run-dir",
        str(run_dir),
    ]
    env = os.environ.copy()
    env["NETHACK_HARNESS"] = spec.harness

    logger.info("launch_gepa: argv=%s", argv)
    process = await asyncio.create_subprocess_exec(
        *argv,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
        cwd=str(repo_root),
        env=env,
    )
    task_id = _make_task_id(spec.label)
    _register_task(task_id, process, time.time())
    return task_id


