def stop_task(task_id: str) -> bool:
    """SIGTERM, then SIGKILL after 5s grace. Returns True if killed.

    Schedules an async waiter; returns True iff a stop signal was delivered to
    a still-live process. (The async escalation continues in the background.)
    """
    task = _TASKS.get(task_id)
    if task is None:
        return False
    proc = task.process
    if proc.returncode is not None:
        return False
    try:
        proc.terminate()
    except ProcessLookupError:
        return False
    except OSError as exc:
        logger.warning("stop_task(%s): terminate failed: %s", task_id, exc)
        return False

    async def _escalate() -> None:
        try:
            await asyncio.wait_for(proc.wait(), timeout=_KILL_GRACE_S)
        except asyncio.TimeoutError:
            logger.warning(
                "stop_task(%s): SIGTERM grace expired, sending SIGKILL", task_id
            )
            try:
                proc.kill()
            except ProcessLookupError:
                pass
            except OSError as exc:
                logger.warning("stop_task(%s): kill failed: %s", task_id, exc)
        except asyncio.CancelledError:
            raise

    try:
        loop = asyncio.get_running_loop()
        loop.create_task(_escalate())
    except RuntimeError:
        # No running loop; cannot schedule escalation. SIGTERM was still sent.
        logger.debug("stop_task(%s): no running loop; skipping SIGKILL escalation", task_id)
    return True


