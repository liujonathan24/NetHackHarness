# Streams
# ---------------------------------------------------------------------------


async def stream_metrics(task_id: str) -> AsyncIterator[TrainMetric]:
    """Yield TrainMetric objects parsed from the subprocess's stdout.

    Replays everything emitted so far, then follows new metrics until the
    process exits (and the log is fully drained).
    """
    if task_id not in _TASKS:
        raise KeyError(task_id)
    task = _TASKS[task_id]
    assert task.metric_cond is not None
    cursor = 0
    while True:
        async with task.metric_cond:
            while cursor >= len(task.metric_log) and not task.finished:
                await task.metric_cond.wait()
            batch = task.metric_log[cursor:]
            cursor = len(task.metric_log)
            done = task.finished
        for item in batch:
            yield item
        if done and cursor >= len(task.metric_log):
            return


async def stream_events(task_id: str) -> AsyncIterator[TaskEvent]:
    """Yield raw stdout/stderr/status/finished events."""
    if task_id not in _TASKS:
        raise KeyError(task_id)
    task = _TASKS[task_id]
    assert task.event_cond is not None
    cursor = 0
    while True:
        async with task.event_cond:
            while cursor >= len(task.event_log) and not task.finished:
                await task.event_cond.wait()
            batch = task.event_log[cursor:]
            cursor = len(task.event_log)
            done = task.finished
        for item in batch:
            yield item
        if done and cursor >= len(task.event_log):
            return


