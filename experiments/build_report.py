"""Roll up experiment JSONs into a Markdown report — the Monday slide.

Reads every experiments/results/*.json and produces a single REPORT.md
with one section per experiment, including verdict, headline number,
and a link to the PNG (if present).

Usage:
    python experiments/build_report.py
    python experiments/build_report.py --out experiments/REPORT.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RESULTS = Path(__file__).resolve().parent / "results"


def _emoji(verdict: str) -> str:
    return {
        "FIX CONFIRMED": "✅",
        "SHIPS": "✅",
        "BASELINE": "📊",
        "INCONCLUSIVE": "❓",
        "ERROR": "❌",
    }.get(verdict, "•")


def _headline(name: str, data: dict) -> str:
    """One-line headline number for each experiment."""
    if name == "exp01_seeding":
        return f"two reruns hash to `{data['fixed_score_two_runs']['run1_hash']}` (was: legacy raised `{data['legacy_challenge_seed_attempt']['raised']}`)"
    if name == "exp02_scout_reward":
        return f"stationary mean: legacy={data['legacy']['stationary_mean']:.4f}/step, fixed={data['fixed']['stationary_mean']:.4f}/step"
    if name == "exp03_menu_masking":
        return f"max row width: legacy={data['legacy_max_row_width']}, fixed={data['fixed_max_row_width']} (menu at col {data['menu_left_col']})"
    if name == "exp04_bootstrap":
        return f"{data['fixed_known_role_count']}/{len(data['seeds'])} seeds parsed role; legacy {data['legacy_known_role_count']}/{len(data['seeds'])}"
    if name == "exp05_terminal_detection":
        cases = data.get("cases", {})
        return f"ascension reward: legacy=0.0 always, fixed={cases.get('ascension', {}).get('fixed_reward', 0):.1f} on win"
    if name == "exp08_autoexplore":
        return f"{data['leverage_ratio']}x more env actions per LM tool-call ({data['fixed']['actions_per_tool_call']} vs {data['legacy']['actions_per_tool_call']})"
    if name == "exp13_code_mode":
        return f"{data['code_mode']['tool_calls']} tool call replaces {data['skill_mode']['tool_calls']} ({data['tool_call_savings']} round-trips saved)"
    if name == "exp14_subgoal_achievement":
        return f"{int(data['achievement_rate'] * 100)}% subgoal achievement on {data['n_seeds']} seeds (autoexplore baseline)"
    if name == "exp15_token_savings":
        return (
            f"{data['obs_compaction_only']['savings_pct_per_turn_obs']}% per-turn obs reduction; "
            f"{data['full_compaction']['savings_pct_cumulative']}% cumulative reduction at turn {data['n_turns']}"
        )
    if name.startswith("baseline_agents"):
        stats = data.get("by_baseline_stats", {})
        parts = [f"{k}={v['mean']}" for k, v in stats.items()]
        return f"reward by baseline: {', '.join(parts)}"
    return "(see JSON)"


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--out", default=str(RESULTS.parent / "REPORT.md"))
    args = p.parse_args()

    if not RESULTS.is_dir():
        print(f"No results dir at {RESULTS}; run experiments first.", file=sys.stderr)
        return 1

    rows = []
    for json_file in sorted(RESULTS.glob("*.json")):
        try:
            data = json.loads(json_file.read_text())
        except Exception as e:
            rows.append({"name": json_file.stem, "verdict": "ERROR", "headline": str(e), "png": None})
            continue
        name = json_file.stem
        verdict = data.get("verdict", "?")
        png = json_file.with_suffix(".png")
        rows.append({
            "name": name,
            "verdict": verdict,
            "headline": _headline(name, data),
            "png": png.name if png.exists() else None,
        })

    lines = [
        "# Experiment Report",
        "",
        f"Auto-generated by `experiments/build_report.py` from `experiments/results/`.",
        f"Run with `python experiments/run_all.py` to refresh JSONs.",
        "",
        "## Summary table",
        "",
        f"| | experiment | verdict | headline |",
        f"|-|-----------|---------|---------|",
    ]
    for r in rows:
        lines.append(f"| {_emoji(r['verdict'])} | `{r['name']}` | {r['verdict']} | {r['headline']} |")

    lines.extend(["", "## Per-experiment detail", ""])
    for r in rows:
        lines.append(f"### {_emoji(r['verdict'])} {r['name']}  —  {r['verdict']}")
        lines.append("")
        lines.append(f"**{r['headline']}**")
        lines.append("")
        if r["png"]:
            lines.append(f"![{r['name']}](results/{r['png']})")
            lines.append("")
        lines.append(f"Raw JSON: `experiments/results/{r['name']}.json`")
        lines.append("")

    out_path = Path(args.out)
    out_path.write_text("\n".join(lines))
    print(f"Wrote {out_path} with {len(rows)} experiments")
    return 0


if __name__ == "__main__":
    sys.exit(main())
